package inheritance22;

public class Date {
	int dd,month,year;
	public void setDd(int dd)
	{
		this.dd = dd;
		
	}
	public void setMonth(int month)
	{
		this.month = month;
	}
	public void setYear(int year)
	{
		this.year = year;
	}
	public int getDd()
	{
		return dd;
	}
	public int getMonth()
	{
		return month;
	}
	public int getYear()
	{
		return year;
	}
	
}
